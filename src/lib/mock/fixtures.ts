function geraDocumento(
  id: string,
  numero: number,
  cliente: Cliente,
  dataIso: string,
  estado: Documento["estado"],
  linhasInput: Array<{ artigo: Artigo; qtd: number }>,
): Documento {
  const linhas = linhasInput.map((l, i) => {
    const total = l.qtd * l.artigo.precoVenda;
    return {
      id: `${id}-l${i + 1}`,
      artigoCodigo: l.artigo.codigo,
      descricao: l.artigo.descricao,
      quantidade: l.qtd,
      precoUnitario: l.artigo.precoVenda,
      taxaIva: l.artigo.taxaIva,
      desconto: 0,
      total,
    };
  });
  const subtotal = linhas.reduce((s, l) => s + l.total, 0);
  const totalIva = linhas.reduce((s, l) => s + l.total * (l.taxaIva / 100), 0);
  const numeroCompleto = `FT A/${String(numero).padStart(6, "0")}`;
  return {
    id,
    tipo: "FT",
    serie: "A",
    numero,
    numeroCompleto,
    dataEmissao: dataIso,
    dataVencimento: dataIso,
    clienteId: cliente.id,
    clienteNome: cliente.nome,
    clienteNif: cliente.nif,
    linhas,
    subtotal,
    totalIva,
    total: subtotal + totalIva,
    moeda: "AOA",
    estado,
    hashDocumento: `SHA256:${id.toUpperCase()}${numero}`,
    qrCode: `A:${cliente.nif}*B:5417829301*C:AO*D:FT*E:N*F:${dataIso}*G:${numeroCompleto}*H:${id}*I1:AO*N:${Math.round(totalIva)}*O:${Math.round(subtotal + totalIva)}`,
    comunicacaoAGT:
      estado === "comunicado_agt"
        ? {
            timestamp: `${dataIso}T10:14:22Z`,
            respostaAGT: "ACK-AGT-2026-OK",
          }
        : null,
  };
}

